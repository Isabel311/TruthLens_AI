def ensure_nltk_resources() -> None:
    """Kept for Colab/classroom readability; preprocessing works without downloads."""
    return None


def get_stop_words() -> set[str]:
    """Use NLTK stopwords when available, otherwise fall back to scikit-learn stopwords."""
    try:
        return set(stopwords.words("english"))
    except LookupError:
        return set(ENGLISH_STOP_WORDS)


@st.cache_resource
def load_artifacts():
    """Load the trained model and TF-IDF vectorizer once per Streamlit session."""
    if not MODEL_FILE.exists() or not VECTORIZER_FILE.exists():
        return None, None
    return joblib.load(MODEL_FILE), joblib.load(VECTORIZER_FILE)



def load_metrics() -> dict:
    """Load saved test metrics produced by train_model.py."""
    if METRICS_FILE.exists():
        return json.loads(METRICS_FILE.read_text(encoding="utf-8"))
    return {}


def preprocess_text(text: str) -> str:
    """Apply lowercasing, punctuation removal, tokenization, and stopword removal."""
    text = text.lower()
    text = text.translate(str.maketrans("", "", string.punctuation))
    tokens = wordpunct_tokenize(text)
    stop_words = get_stop_words()
    cleaned_tokens = [word for word in tokens if word.isalpha() and word not in stop_words]
    return " ".join(cleaned_tokens)


def predict_news(text: str, model, vectorizer) -> dict:
    """Predict whether news text is real or fake and return class probabilities."""
    cleaned_text = preprocess_text(text)
    transformed_text = vectorizer.transform([cleaned_text])
    prediction = int(model.predict(transformed_text)[0])

    if hasattr(model, "predict_proba"):
        probabilities = model.predict_proba(transformed_text)[0]
        fake_probability = float(probabilities[0])
        real_probability = float(probabilities[1])
    else:
        decision_score = float(model.decision_function(transformed_text)[0])
        real_probability = 1 / (1 + pow(2.71828, -decision_score))
        fake_probability = 1 - real_probability

    predicted_label = "Real" if prediction == 1 else "Fake"
    confidence = real_probability if prediction == 1 else fake_probability
    credibility = real_probability

    return {
        "label": predicted_label,
        "confidence": confidence,
        "credibility": credibility,
        "fake_probability": fake_probability,
        "real_probability": real_probability,
        "cleaned_text": cleaned_text,
    }


def save_history(news_text: str, result: dict) -> None:
    """Append the current prediction to a CSV history file."""
    row = {
        "timestamp": datetime.now().strftime("%Y-%m-%d %H:%M:%S"),
        "prediction": result["label"],
        "confidence_percent": round(result["confidence"] * 100, 2),
        "credibility_percent": round(result["credibility"] * 100, 2),
        "news_text": news_text[:500],
    }

    new_entry = pd.DataFrame([row])
    if HISTORY_FILE.exists():
        history = pd.read_csv(HISTORY_FILE)
        history = pd.concat([new_entry, history], ignore_index=True)
    else:
        history = new_entry
    history.to_csv(HISTORY_FILE, index=False)


def build_report(news_text: str, result: dict, metrics: dict) -> str:
    """Create a downloadable plain-text prediction report."""
    accuracy = metrics.get("accuracy")
    accuracy_text = f"{accuracy * 100:.2f}%" if isinstance(accuracy, (int, float)) else "Not available"

    return f"""TruthLens AI - Fake News Detection Report
Generated At: {datetime.now().strftime("%Y-%m-%d %H:%M:%S")}

Prediction: {result["label"]} News
Confidence Score: {result["confidence"] * 100:.2f}%
News Credibility: {result["credibility"] * 100:.2f}%
Fake Probability: {result["fake_probability"] * 100:.2f}%
Real Probability: {result["real_probability"] * 100:.2f}%
Model Test Accuracy: {accuracy_text}

Input News:
{news_text}

Cleaned Text:
{result["cleaned_text"]}

Note:
This result is generated by a machine learning model and should support, not replace,
human fact-checking and verification from trusted sources.
"""


