def render_sidebar(metrics: dict) -> None:
    """Render project information and model metrics in the sidebar."""
    st.sidebar.title("TruthLens AI")
    st.sidebar.caption("Fast NLP fake news detection using TF-IDF and Logistic Regression.")

    accuracy = metrics.get("accuracy")
    if isinstance(accuracy, (int, float)):
        st.sidebar.metric("Test Accuracy", f"{accuracy * 100:.2f}%")
    else:
        st.sidebar.info("Train the model to show test accuracy.")

    st.sidebar.divider()
    st.sidebar.write("Pipeline")
    st.sidebar.write("1. Clean text")
    st.sidebar.write("2. Remove stopwords")
    st.sidebar.write("3. TF-IDF vectorization")
    st.sidebar.write("4. Logistic Regression")

    st.sidebar.divider()
    st.sidebar.warning("Always verify important news with trusted sources.")


def render_history() -> None:
    """Show recent prediction history if available."""
    st.subheader("Analysis History")
    if HISTORY_FILE.exists():
        history = pd.read_csv(HISTORY_FILE).head(10)
        st.dataframe(history, use_container_width=True, hide_index=True)
    else:
        st.caption("No predictions analyzed yet.")


